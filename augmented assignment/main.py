# Demonstration of all augmented assignment operators in Python

# Addition assignment
x = 10
x += 5  # Equivalent to x = x + 5
print("x += 5:", x)  # Output: 15

# Subtraction assignment
y = 20
y -= 3  # Equivalent to y = y - 3
print("y -= 3:", y)  # Output: 17

# Multiplication assignment
z = 7
z *= 2  # Equivalent to z = z * 2
print("z *= 2:", z)  # Output: 14

# Division assignment
a = 16
a /= 4  # Equivalent to a = a / 4
print("a /= 4:", a)  # Output: 4.0

# Modulo assignment
b = 10
b %= 3  # Equivalent to b = b % 3
print("b %= 3:", b)  # Output: 1

# Exponentiation assignment
c = 2
c **= 3  # Equivalent to c = c ** 3
print("c **= 3:", c)  # Output: 8

# Floor division assignment
d = 15
d //= 2  # Equivalent to d = d // 2
print("d //= 2:", d)  # Output: 7

# Bitwise AND assignment
e = 6
e &= 3  # Equivalent to e = e & 3
print("e &= 3:", e)  # Output: 2

# Bitwise OR assignment
f = 4
f |= 1  # Equivalent to f = f | 1
print("f |= 1:", f)  # Output: 5

# Bitwise XOR assignment
g = 5
g ^= 2  # Equivalent to g = g ^ 2
print("g ^= 2:", g)  # Output: 7

# Bitwise left shift assignment
h = 3
h <<= 1  # Equivalent to h = h << 1
print("h <<= 1:", h)  # Output: 6

# Bitwise right shift assignment
i = 8
i >>= 2  # Equivalent to i = i >> 2
print("i >>= 2:", i)  # Output: 2
