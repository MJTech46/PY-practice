# Example of ANSI escape codes
print("\033[31mThis text is red!\033[0m")  # Red text
print("\033[32mThis text is green!\033[0m")  # Green text
print("\033[33mThis text is yellow!\033[0m")  # Yellow text
print("\033[34mThis text is blue!\033[0m")   # Blue text
print("\033[1m\033[31mBold and Red Text!\033[0m")  # Bold and red text
